// Get saved language or detect from browser
function getSavedLanguage() {
    const saved = localStorage.getItem('ibuilt-language');
    if (saved && ['en', 'fr', 'ht'].includes(saved)) {
        return saved;
    }
    
    // Detect from browser
    const browserLang = navigator.language.substring(0, 2);
    if (browserLang === 'fr') return 'fr';
    if (browserLang === 'ht') return 'ht';
    return 'en';
}

// Save language preference
function saveLanguage(lang) {
    localStorage.setItem('ibuilt-language', lang);
}

// Get translation
function t(key) {
    const lang = getSavedLanguage();
    if (translations[key] && translations[key][lang]) {
        return translations[key][lang];
    }
    // Fallback to English
    if (translations[key] && translations[key]['en']) {
        return translations[key]['en'];
    }
    return key;
}

// Apply translations to page
function applyTranslations() {
    const lang = getSavedLanguage();
    
    // Update HTML lang attribute
    document.documentElement.lang = lang;
    
    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[key] && translations[key][lang]) {
            el.textContent = translations[key][lang];
        }
    });
    
    // Update elements with data-i18n-placeholder attribute
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (translations[key] && translations[key][lang]) {
            el.placeholder = translations[key][lang];
        }
    });
    
    // Update elements with data-i18n-title attribute
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
        const key = el.getAttribute('data-i18n-title');
        if (translations[key] && translations[key][lang]) {
            el.title = translations[key][lang];
        }
    });
    
    // Update language selector to show current language
    const langSelector = document.getElementById('language-selector');
    if (langSelector) {
        langSelector.value = lang;
    }
    
    // Update language buttons styling
    document.querySelectorAll('[data-lang-btn]').forEach(btn => {
        const btnLang = btn.getAttribute('data-lang-btn');
        if (btnLang === lang) {
            btn.classList.add('bg-primary', 'text-white');
            btn.classList.remove('bg-border-dark', 'text-slate-300', 'hover:bg-primary/20');
        } else {
            btn.classList.remove('bg-primary', 'text-white');
            btn.classList.add('bg-border-dark', 'text-slate-300', 'hover:bg-primary/20');
        }
    });
}

// Change language
function changeLanguage(lang) {
    if (['en', 'fr', 'ht'].includes(lang)) {
        saveLanguage(lang);
        applyTranslations();
    }
}

// Initialize on page load
document.addEventListener('DOMContentLoaded', function() {
    applyTranslations();
});

// Export for use in HTML
window.changeLanguage = changeLanguage;
window.t = t;
window.getSavedLanguage = getSavedLanguage;
